/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class Solution {
    public TreeNode lowestCommonAncestor(TreeNode root, TreeNode p, TreeNode q) {


        if (root != p && root != q) {
            if (root == null) {
                return null;
            }
            if ((findAncestor(root.left, p) && findAncestor(root.right, q)) || (findAncestor(root.left, q) && findAncestor(root.right, p))) {
                return root;
            } else {
                TreeNode leftNode = lowestCommonAncestor(root.left, p, q);

                TreeNode rightNode = lowestCommonAncestor(root.right, p, q);


                return leftNode != null ? leftNode : rightNode;
            }
        }
        return root;

    }
    private boolean findAncestor(TreeNode cur, TreeNode p) {
        if (cur == null) {
            return false;
        }
        if (cur == p) {
            return true;
        }

        return findAncestor(cur.left, p) || findAncestor(cur.right, p);
    }
}


