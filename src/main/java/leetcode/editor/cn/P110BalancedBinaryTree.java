/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public boolean isBalanced(TreeNode root) {
        isBalancedDg(root);
        return flag;
    }

    boolean flag = true;
    private int isBalancedDg(TreeNode root) {

        if (root == null) {
            return 0;
        }

        if (!flag) {
            return 0;
        }
        int left = root.left !=null ? 1 + isBalancedDg(root.left) : isBalancedDg(root.left);

        int right = root.right != null ? 1 + isBalancedDg(root.right) : isBalancedDg(root.right);

        if (Math.abs(left - right) <= 1) {
            flag = true;
        } else {
            flag = false;
        }
        return Math.max(left, right);
    }
}
