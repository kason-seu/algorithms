/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode getKthFromEnd(ListNode head, int k) {

        return dg(head, k);
    }

    int m = 0;
    private ListNode dg(ListNode cur, int k) {


        if (cur != null) {
            ListNode dg = dg(cur.next, k);

            if (m == k) {
                m++;
                //cur.next = dg.next;
                return dg;
            } else if (m < k){
                cur.next = dg;
                m++;
                return cur;
            } else {
                m++;
                return dg;
            }

        } else {
            return null;
        }
    }
}
